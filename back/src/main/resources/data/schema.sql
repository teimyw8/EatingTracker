CREATE TABLE IF NOT EXISTS MEAL(
ID UUID PRIMARY KEY,
NAME TEXT
);
CREATE TABLE IF NOT EXISTS INGR(
ID UUID PRIMARY KEY,
NAME TEXT,
PROTEIN DOUBLE,
CALS DOUBLE,
SERVG DOUBLE
);
CREATE TABLE IF NOT EXISTS MEALINGR(
ingrId UUID,
mealId UUID,
amm FLOAT,
PRIMARY KEY (ingrId,mealId)
);
CREATE TABLE IF NOT EXISTS CURRENTDAY(
TODAY DATE PRIMARY KEY
);
CREATE TABLE IF NOT EXISTS EATENLIST(
TYPE ENUM('meal', 'ingr'),
dayID DATE,
ITEMID UUID,
PRIMARY KEY (type, dayID, itemID)
);

